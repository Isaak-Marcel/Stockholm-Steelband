            <p class="p2 lg:text-left text-center">Stockholm Steelband har en lång historia. Steelbandet Hot Pans bildades 1984, efter att ett steelband från Trinidad gjort en turné i Sverige och därefter lämnat de instrument som användes. Cirka 10 år senare utökades steelbandsfamiljen med bandet Cool Pans. 2014 gick banden samma och bildade Stockholm Steelband. Förutom bandets musiker, anlitas arrangörer för att ständigt utöka och utveckla bandets repertoar. Bandet har också samarbetat och uppträtt tillsammans med andra artister och band, ex Kaliffa. Den legendariska steelpanmusikern, Rudy “Two Left” Smith, har byggt de flesta av bandets instrument och arrangerat en del av låtarna.</p>
